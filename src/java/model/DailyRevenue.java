package model;
import java.math.BigDecimal;
import java.sql.Date;
/**
 *
 * @author TNO
 */
public class DailyRevenue {
    private Date date;
    private BigDecimal amount;

    public DailyRevenue() {
    }

    public DailyRevenue(Date date, BigDecimal amount) {
        this.date = date;
        this.amount = amount;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public BigDecimal getAmount() {
        return amount;
    }

    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }
    
    
    
    
    
}
